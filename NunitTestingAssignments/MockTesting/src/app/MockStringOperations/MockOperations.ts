export class MockOperatios
{
    convertToUpperCase(string:any)
    {
        return string.toUpperCase();
    }
    convertToLowerCase(string:any)
    {
        return string.toLowerCase();
    }
    convertToString(string:any)
    {
        return string.toString();
    }
    Concat(string1:any,string2:any)
    {
        return string1.concat(string2);
    }
    replace(string1:any,string2:any)
    {
        return string1.replace(string2);
    }

}